import zipfile
import os

heart_zip_file_path = '/content/archive (4).zip'
heart_extract_dir = '/content/extracted_heart_dataset'

os.makedirs(heart_extract_dir, exist_ok=True)

try:
    with zipfile.ZipFile(heart_zip_file_path, 'r') as zip_ref:
        zip_ref.extractall(heart_extract_dir)

    print(f"Contents extracted to: {heart_extract_dir}")

    print("Extracted files:")
    for root, dirs, files in os.walk(heart_extract_dir):
        for file in files:
            print(os.path.join(root, file))

except FileNotFoundError:
    print(f"Error: The file '{heart_zip_file_path}' was not found. Please ensure the file is uploaded correctly.")
except Exception as e:
    print(f"An error occurred during extraction: {e}")

import pandas as pd

heart_dataset_path = '/content/extracted_heart_dataset/heart_disease_uci.csv'

try:
    df_heart = pd.read_csv(heart_dataset_path)

    print("### Head of the Heart Disease Dataset:")
    print(df_heart.head())
    print("\n" + "-"*30 + "\n")

    print("### Info of the Heart Disease Dataset:")
    print(df_heart.info())
    print("\n" + "-"*30 + "\n")

    print("### Description of the Heart Disease Dataset:")
    print(df_heart.describe())
    print("\n" + "-"*30 + "\n")

except FileNotFoundError:
    print(f"Error: The file '{heart_dataset_path}' was not found. Please ensure the file exists.")
except Exception as e:
    print(f"An error occurred while loading or inspecting the dataset: {e}")

print("Checking for '?' in object columns:")
for col in df_heart.select_dtypes(include='object').columns:
    if '?' in df_heart[col].unique():
        print(f"  Column '{col}' contains '?'. Replacing with NaN.")
        df_heart[col] = df_heart[col].replace('?', pd.NA)


print("\nMissing values before imputation:")
print(df_heart.isnull().sum())

print("\nUnique values in object columns:")
for col in df_heart.select_dtypes(include='object').columns:
    print(f"Column '{col}': {df_heart[col].unique()}")

df_heart['fbs'] = df_heart['fbs'].astype(bool).astype(int)

df_heart['target'] = df_heart['num'].apply(lambda x: 1 if x > 0 else 0)

print("\nDistribution of the binarized target variable 'target':")
print(df_heart['target'].value_counts())
print("\nTarget column unique values:", df_heart['target'].unique())

numerical_cols_for_imputation = ['trestbps', 'chol', 'thalch', 'oldpeak']

for col in numerical_cols_for_imputation:
    if df_heart[col].isnull().any():
        mean_value = df_heart[col].mean()
        df_heart[col].fillna(mean_value, inplace=True)
        print(f"Imputed missing values in '{col}' with mean: {mean_value:.2f}")

if df_heart['ca'].isnull().any():

    df_heart['ca'] = pd.to_numeric(df_heart['ca'], errors='coerce')

    mode_value_ca = df_heart['ca'].mode()[0]
    df_heart['ca'].fillna(mode_value_ca, inplace=True)
    print(f"Imputed missing values in 'ca' with mode: {mode_value_ca}")

if df_heart['thal'].isnull().any():
    mode_value_thal = df_heart['thal'].mode()[0]
    df_heart['thal'].fillna(mode_value_thal, inplace=True)
    print(f"Imputed missing values in 'thal' with mode: {mode_value_thal}")

for col in ['restecg', 'exang', 'slope']:
    if df_heart[col].isnull().any():
        df_heart[col].fillna('Unknown', inplace=True)
        print(f"Filled NaN in '{col}' with 'Unknown'")

df_heart.drop(['id', 'num'], axis=1, inplace=True)

categorical_cols = df_heart.select_dtypes(include='object').columns.tolist()

df_heart_encoded = pd.get_dummies(df_heart, columns=categorical_cols, drop_first=True, dtype=int)

print("\nMissing values after imputation:")
print(df_heart_encoded.isnull().sum().sum())

print("\nDataFrame head after preprocessing:")
print(df_heart_encoded.head())
print("\nDataFrame info after preprocessing:")
print(df_heart_encoded.info())

from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score

X_heart = df_heart_encoded.drop('target', axis=1)
y_heart = df_heart_encoded['target']

X_train_heart, X_test_heart, y_train_heart, y_test_heart = train_test_split(X_heart, y_heart, test_size=0.2, random_state=42)

print(f"Shape of X_train_heart: {X_train_heart.shape}")
print(f"Shape of X_test_heart: {X_test_heart.shape}")
print(f"Shape of y_train_heart: {y_train_heart.shape}")
print(f"Shape of y_test_heart: {y_test_heart.shape}")

log_reg_heart = LogisticRegression(max_iter=1000, random_state=42)
log_reg_heart.fit(X_train_heart, y_train_heart)

y_pred_log_reg_heart = log_reg_heart.predict(X_test_heart)

accuracy_log_reg_heart = accuracy_score(y_test_heart, y_pred_log_reg_heart)
print(f"\nLogistic Regression Accuracy for Heart Disease: {accuracy_log_reg_heart:.4f}")

"""### Scaling Features and Re-evaluating Logistic Regression"""

from sklearn.preprocessing import StandardScaler

scaler = StandardScaler()

X_train_heart_scaled = scaler.fit_transform(X_train_heart)
X_test_heart_scaled = scaler.transform(X_test_heart)

print("Features scaled successfully.")


log_reg_heart_scaled = LogisticRegression(max_iter=1000, random_state=42)
log_reg_heart_scaled.fit(X_train_heart_scaled, y_train_heart)

y_pred_log_reg_heart_scaled = log_reg_heart_scaled.predict(X_test_heart_scaled)

accuracy_log_reg_heart_scaled = accuracy_score(y_test_heart, y_pred_log_reg_heart_scaled)
print(f"\nLogistic Regression Accuracy (Scaled Data) for Heart Disease: {accuracy_log_reg_heart_scaled:.4f}")

results_heart = {'Logistic Regression (Scaled)': accuracy_log_reg_heart_scaled}

"""### Training and Evaluating Other Models"""

from sklearn.svm import SVC
from sklearn.ensemble import RandomForestClassifier
import xgboost as xgb

print("\nTraining Support Vector Machine...")
svm_heart = SVC(random_state=42)
svm_heart.fit(X_train_heart_scaled, y_train_heart)
y_pred_svm_heart = svm_heart.predict(X_test_heart_scaled)
accuracy_svm_heart = accuracy_score(y_test_heart, y_pred_svm_heart)
print(f"SVM Accuracy for Heart Disease: {accuracy_svm_heart:.4f}")
results_heart['SVM'] = accuracy_svm_heart

print("\nTraining Random Forest Classifier...")
rf_heart = RandomForestClassifier(random_state=42)
rf_heart.fit(X_train_heart_scaled, y_train_heart)
y_pred_rf_heart = rf_heart.predict(X_test_heart_scaled)
accuracy_rf_heart = accuracy_score(y_test_heart, y_pred_rf_heart)
print(f"Random Forest Accuracy for Heart Disease: {accuracy_rf_heart:.4f}")
results_heart['Random Forest'] = accuracy_rf_heart

print("\nTraining XGBoost Classifier...")

xgb_heart = xgb.XGBClassifier(objective='binary:logistic', eval_metric='logloss', use_label_encoder=False, random_state=42, enable_categorical=True, tree_method='hist')
xgb_heart.fit(X_train_heart_scaled, y_train_heart)
y_pred_xgb_heart = xgb_heart.predict(X_test_heart_scaled)
accuracy_xgb_heart = accuracy_score(y_test_heart, y_pred_xgb_heart)
print(f"XGBoost Accuracy for Heart Disease: {accuracy_xgb_heart:.4f}")
results_heart['XGBoost'] = accuracy_xgb_heart

print("\n" + "="*40)
print("### Heart Disease Model Performance Comparison ###")
for model, accuracy in results_heart.items():
    print(f"{model}: {accuracy:.4f}")
print("="*40)